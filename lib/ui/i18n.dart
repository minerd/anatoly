/// Geçerli dil için metinleri sağlar (eksik anahtarda İngilizce'ye, sonra anahtara düşer).
class Strings {
  final String code;
  final Map<String, String> _m;
  const Strings(this.code, this._m);

  String s(String key) => _m[key] ?? _catalog['en']![key] ?? key;

  /// Parametreli çeviri: `f('home.exercisesN', {'n': '4'})` -> "4 egzersiz".
  String f(String key, Map<String, String> params) {
    var v = s(key);
    params.forEach((k, val) => v = v.replaceAll('{$k}', val));
    return v;
  }

  /// Kısa ay adı (1-12).
  String month(int m) => _months[code]?[m - 1] ?? _months['en']![m - 1];
}

Strings stringsFor(String code) =>
    Strings(code, _catalog[code] ?? _catalog['en']!);

